weight = float(input('Your weight: '))

height = float(input('Your height: '))

bmi = weight / (height * height)


if bmi < 18.5:
   print('Underweight')
elif bmi <= 24.9 :
   print('normal weight')
elif bmi <= 25.0:
   print('overweight')
elif bmi > 65:
   print('class 1 obesity')
elif bmi > 65:
   print('class 2 obesity')
else:
   print('class 3 obesity')


